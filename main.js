function combineStringAndNumber(text, number) {
    return text + number;
}
console.log(combineStringAndNumber("age:", 19));
function reminder(num1, num2) {
    return num1 % num2;
}
console.log(reminder(4, 2));
console.log(reminder(5, 3));
console.log(reminder(4, 3));
function checkBothTrue(val1, val2) {
    return val1 && val2;
}
console.log(checkBothTrue(true, false));
